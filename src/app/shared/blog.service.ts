import { Injectable } from '@angular/core';
import { Subject, Observable } from 'rxjs/Rx';
import { IBlog, IComment, ITag } from './blog.model';

@Injectable()
export class BlogService {
    getBlogs(): Observable<IBlog[]> {
    const subject = new Subject<IBlog[]>();
    setTimeout(() => {subject.next(BLOGS); subject.complete(); }, 100);
    return subject;
  }
  getBlogArticle(id: number): IBlog {
    return BLOGS.find(event => event.id === id);
  }

//   getBlogComment(id: number): IComment {
//     return BLOGS.find(event => event.id === id);
//   }


  updateBlog(blog) {
    const index = BLOGS.findIndex(x => x.id = blog.id);
    BLOGS[index] = blog;
  }

}

